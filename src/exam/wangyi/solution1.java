package Algorithm.exam.wangyi;

import java.util.Scanner;

public class solution1 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] p = new int[n];
        for (int i = 0; i < n; i++) {
            p[i] = sc.nextInt();
        }
        //每次交换数字相邻的数
        //最终得到递增数列
        //交换逆序
        //1,6,4,5,3，2

    }
}
